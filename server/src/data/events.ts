// Generate timestamps relative to now
function hoursAgo(h: number): number {
  return Math.floor((Date.now() - h * 3600 * 1000) / 1000);
}

function daysAgo(d: number, h: number = 0): number {
  return Math.floor((Date.now() - (d * 86400 + h * 3600) * 1000) / 1000);
}

